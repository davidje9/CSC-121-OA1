first_name = "kazuo"
last_name = "ishiguro"
full_name = f"{first_name} {last_name}"

#print full name in lowercase
print(f"Full name in lowercase: \t{full_name.lower()}")

#print full name in uppercase
print(f"Full name in uppercase: \t{full_name.upper()}")

#print full name in title case
print(f"Full name in title case:\t{full_name.title()}")
